package com.rekik.roboresumeapril;

public class Skill {
    private String skillName;
    private int level;

    public String getSkillName() {
        return skillName;
    }

    public void setSkillName(String skillName) {
        this.skillName=skillName;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level=level;
    }
}
